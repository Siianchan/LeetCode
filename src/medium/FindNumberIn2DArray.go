func findNumberIn2DArray(matrix [][]int, target int) bool {
	if matrix == nil || len(matrix) == 0 || len(matrix[0]) == 0 {
		return false
	}
	x := 0
	y := len(matrix[0]) - 1
	for x < len(matrix) && y >= 0 { //从右上角开始查找，当大于时，往左移动一格，当小于时，往下移动一格
		if matrix[x][y] == target {
			return true
		} else if matrix[x][y] > target {
			y--
		} else {
			x++
		}
	}
	return false
}
